
public class Person {

	private String name;
	private int edad;
	
	
	public Person() {
		
		
		
	}

	public Person(String name, int edad) {
		
		this.name = name;
		this.edad = edad;
	}

	public String getName() {
		return name;
	}

	

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	
	
}
